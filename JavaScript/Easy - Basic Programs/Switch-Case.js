// Grade A -> Excellent!
// Grade B -> Very Good!
// Grade C -> Good Job!
// Grade D -> Not Bad!
// Grade F -> Failed!

let grade = "A";

switch (grade) {
  case "A":
    console.log("Excellent!");
    break;
  case "B":
    console.log("Very Good");
    break;
  case "C":
    console.log("Good Job");
    break;
  case "D":
    console.log("Not Bad!");
    break;
  case "F":
    console.log(" Failed!");
    break;
  default:
    console.log("Invalid Grade");
}

// Output -> Excellent!