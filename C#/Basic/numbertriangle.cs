namespace numbertriangle{
    class program{
        static void main(string [] args){
            for(int i=0;i<10;i++){
                for(int j=0;j<=i;j++){
                    Console.WriteLine("*");
                }
                Console.WriteLine(" ");
            }
        }
    }
}